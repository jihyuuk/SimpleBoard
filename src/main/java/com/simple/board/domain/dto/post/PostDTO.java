package com.simple.board.domain.dto.post;

import com.simple.board.domain.entity.Post;
import lombok.*;

@Getter @Setter
@NoArgsConstructor
@AllArgsConstructor
public class PostDTO {

    Long id;
    String title;
    String content;


    public PostDTO(Post post) {
        this.id = post.getId();
        this.title = post.getTitle();
        this.content = post.getContent();
    }
}
